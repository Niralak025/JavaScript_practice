//create an empty array with constructor
let array1 = new Array()

console.log("array1====",array1); //[]
console.log("array1====",typeof array1); //object
console.log("array1====",array1.length); //0

//create an empty array with size 5 using constructor
let array2 = new Array(5)

console.log("array2====",array2); //[ <5 empty items> ]
console.log("array2====",typeof array2); //object
console.log("array2====",array2.length); //5

//Also create an empty array
let array3 = []

console.log("array2====",array3); //[ <5 empty items> ]
console.log("array2====",typeof array3); //object
